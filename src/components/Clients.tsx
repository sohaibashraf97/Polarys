type LogoParams = {
  fontSize: number;
  opacity: number;
  animSpeed: number;
  animIntensity: number;
  animMode: string;
  color: string;
  labelSize: number;
};

type ClientConfig = {
  name: string;
  artKey: string;
  seed: number;
  href: string;
  titleSrc: string;
};

function AsciiLogo({
  name,
  artKey,
  seed,
  titleSrc,
  titleOffsetY,
  params,
  isHovered,
}: {
  name: string;
  artKey?: string;
  seed: number;
  titleSrc: string;
  titleOffsetY: number;
  params: LogoParams;
  isHovered: boolean;
}) {
  const grid = artKey ? GRIDS[artKey] : null;

  const [lines, setLines] = useState<string[]>(() =>
    grid
      ? grid.map((row) => row.map((on) => (on ? "*" : " ")).join(""))
      : []
  );

  const rafRef = useRef<number>(0);
  const t0Ref = useRef<number | null>(null);

  useEffect(() => {
    if (!grid) return;
    if (isHovered) {
      setLines(grid.map((row) => row.map((on) => (on ? "*" : " ")).join("")));
      return;
    }

    const tick = (ts: number) => {
      if (t0Ref.current === null) t0Ref.current = ts;
      const t = (ts - t0Ref.current) / 1000;
      setLines(renderFrame(grid, t * params.animSpeed, seed, params.animMode, params.animIntensity));
      rafRef.current = requestAnimationFrame(tick);
    };
    rafRef.current = requestAnimationFrame(tick);
    return () => cancelAnimationFrame(rafRef.current);
  }, [grid, seed, params.animSpeed, params.animIntensity, params.animMode, isHovered]);

  return (
    <div className="inline-flex w-full max-w-[280px] flex-col items-center text-center">
      <div className="flex h-[160px] w-full items-center justify-center">
        {grid ? (
          <pre
            className="font-mono select-none mx-auto"
            style={{
              fontSize: `${params.fontSize}px`,
              lineHeight: 1.05,
              opacity: params.opacity,
              letterSpacing: 0,
              color: params.color,
            }}
            aria-hidden="true"
          >
            {lines.join("\n")}
          </pre>
        ) : (
          <div
            className="font-mono opacity-30 text-xs"
            style={{ color: params.color }}
            aria-hidden="true"
          >
            ░░░░░░░░░░░░
            <br />
            ░░░░░░░░░░░░
            <br />
            ░░░░░░░░░░░░
          </div>
        )}
      </div>
      <div
        className="flex w-full items-center justify-center"
        style={{ marginTop: `${titleOffsetY}px` }}
      >
        <img
          src={titleSrc}
          alt={`${name} title`}
          className="h-6 w-auto object-contain"
          style={{
            opacity: 1,
            filter: "brightness(0) saturate(100%) invert(100%)",
          }}
        />
      </div>
    </div>
  );
}

