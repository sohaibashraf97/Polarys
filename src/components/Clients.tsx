"use client";

import { useEffect, useRef, useState } from "react";

type BoolGrid = boolean[][];

// All logos are normalised to this grid before rendering.
const TARGET_W = 80;
const TARGET_H = 40;

/** Parse ASCII art string — any non-space char = on, space/undefined = off. Trims blank lines. */
function parseArt(art: string): BoolGrid {
  const lines = art.split("\n");
  let s = 0, e = lines.length - 1;
  while (s < e && lines[s].trim() === "") s++;
  while (e > s && lines[e].trim() === "") e--;
  const trimmed = lines.slice(s, e + 1);
  const width = Math.max(...trimmed.map((l) => l.length));
  return trimmed.map((line) =>
    Array.from({ length: width }, (_, i) => line[i] !== " " && line[i] !== undefined)
  );
}

/**
 * Shrink a grid to fit within TARGET_W × TARGET_H, preserving aspect ratio.
 * The result is centered in the target canvas with empty padding.
 */
function normaliseGrid(grid: BoolGrid): BoolGrid {
  const srcH = grid.length;
  const srcW = grid[0]?.length ?? 0;
  const out: BoolGrid = Array.from({ length: TARGET_H }, () => Array(TARGET_W).fill(false));
  if (srcH === 0 || srcW === 0) return out;

  // Uniform scale: only shrink, never enlarge
  const scale = Math.min(1, TARGET_W / srcW, TARGET_H / srcH);
  const dstW = Math.round(srcW * scale);
  const dstH = Math.round(srcH * scale);

  // Centre offset so the logo sits in the middle of the canvas
  const offX = Math.floor((TARGET_W - dstW) / 2);
  const offY = Math.floor((TARGET_H - dstH) / 2);

  for (let ty = 0; ty < dstH; ty++) {
    const sy0 = (ty * srcH) / dstH;
    const sy1 = ((ty + 1) * srcH) / dstH;
    for (let tx = 0; tx < dstW; tx++) {
      const sx0 = (tx * srcW) / dstW;
      const sx1 = ((tx + 1) * srcW) / dstW;
      let total = 0, on = 0;
      for (let sy = Math.floor(sy0); sy < Math.ceil(sy1); sy++)
        for (let sx = Math.floor(sx0); sx < Math.ceil(sx1); sx++)
          if (sy < srcH && sx < srcW) { total++; if (grid[sy][sx]) on++; }
      out[offY + ty][offX + tx] = total > 0 && on / total > 0.35;
    }
  }
  return out;
}

/** Render one animation frame. mode controls the effect, intensity is a 0–1 threshold. */
function renderFrame(
  grid: BoolGrid,
  t: number,
  seed: number,
  mode: string,
  intensity: number
): string[] {
  const H = grid.length;
  const W = grid[0]?.length ?? 0;
  const cx = W / 2, cy = H / 2;

  return grid.map((row, py) =>
    row.map((on, px) => {
      if (!on) return " ";
      let bright = true;

      switch (mode) {
        // Diagonal wave + per-pixel noise
        case "shimmer": {
          const wave  = Math.sin(px * 0.12 + py * 0.08 - t * 1.1 + seed) * 0.5 + 0.5;
          const noise = Math.sin(px * 6.3  + py * 4.9  + t * 5.1  + seed * 2.3) * 0.5 + 0.5;
          bright = wave * 0.65 + noise * 0.35 > intensity;
          break;
        }
        // Whole logo breathes in/out together
        case "pulse": {
          const p = Math.sin(t * 2.0 + seed) * 0.5 + 0.5;
          const jitter = Math.sin(px * 3.1 + py * 2.7 + seed) * 0.08;
          bright = p + jitter > intensity;
          break;
        }
        // Horizontal scanline sweeps top → bottom
        case "scan": {
          const scanY = ((t * 0.4) % 1) * H;
          const d = Math.abs(py - scanY);
          bright = d < 2.5 || Math.sin(px * 1.9 + py * 1.1 + seed) * 0.5 + 0.5 > intensity + 0.25;
          break;
        }
        // Random block-level dropout — digital corruption
        case "glitch": {
          const bx = Math.floor(px / 7);
          const by = Math.floor(py / 4);
          const bn = Math.sin(bx * 4.7 + by * 3.1 + Math.floor(t * 4) * 9.3 + seed) * 0.5 + 0.5;
          bright = bn > intensity * 0.75;
          break;
        }
        // Each column has a falling raindrop + faint trail
        case "rain": {
          const phase   = Math.sin(px * 5.3 + seed * 2.1) * 0.5 + 0.5;
          const dropY   = ((t * 0.6 + phase) % 1) * H;
          const trail   = py - dropY;
          const sparkle = Math.sin(px * 4.1 + py * 7.9 + t * 3.3 + seed) > 0.85;
          bright = (trail > -4 && trail <= 0) || sparkle;
          break;
        }
        // Circular ripple radiating from centre
        case "ripple": {
          const dist  = Math.sqrt((px - cx) ** 2 + ((py - cy) * 2) ** 2);
          const wave  = Math.sin(dist * 0.55 - t * 2.5 + seed) * 0.5 + 0.5;
          bright = wave > intensity;
          break;
        }
        // Fast pure-random per-pixel flicker
        case "static noise": {
          const n = Math.sin(px * 127.1 + py * 311.7 + t * 31.3 + seed * 17.1) * 0.5 + 0.5;
          bright = n > intensity;
          break;
        }
        // Vertical wipe left → right on repeat
        case "wipe": {
          const wipeX = ((t * 0.35) % 1) * W;
          bright = px < wipeX || Math.sin(px * 2.3 + py * 1.7 + seed) * 0.5 + 0.5 > intensity + 0.3;
          break;
        }
        // Solid — no animation
        case "solid":
        default:
          bright = true;
      }

      return bright ? "*" : "·";
    }).join("")
  );
}

