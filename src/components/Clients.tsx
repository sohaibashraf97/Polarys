function ClientLogoItem({
  client,
  baseParams,
  logoScale,
  titleOffsetY,
  logoGapX,
}: {
  client: ClientConfig;
  baseParams: LogoParams;
  logoScale: number;
  titleOffsetY: number;
  logoGapX: number;
}) {
  const [isHovered, setIsHovered] = useState(false);

  const defaultsByClient: Record<string, {
    animationSpeed: number;
    sizeLogo: number;
    sizeLabel: number;
    spacingPadding: number;
  }> = {
    entropy: {
      animationSpeed: 0.1,
      sizeLogo: 2.2,
      sizeLabel: 12,
      spacingPadding: 0,
    },
    gryd: {
      animationSpeed: 0.1,
      sizeLogo: 2.4,
      sizeLabel: 12,
      spacingPadding: 13,
    },
    HEX: {
      animationSpeed: 0.1,
      sizeLogo: 2.5,
      sizeLabel: 12,
      spacingPadding: 9,
    },
    milkstraw: {
      animationSpeed: 0.1,
      sizeLogo: 2.5,
      sizeLabel: 12,
      spacingPadding: -24,
    },
  };

  const defaults = defaultsByClient[client.name] ?? {
    animationSpeed: baseParams.animSpeed,
    sizeLogo: baseParams.fontSize,
    sizeLabel: baseParams.labelSize,
    spacingPadding: 2,
  };

  const logoParams: LogoParams = {
    ...baseParams,
    animSpeed: defaults.animationSpeed,
    fontSize: defaults.sizeLogo * logoScale,
    labelSize: defaults.sizeLabel,
    color: "#FFFFFF",
  };

  return (
    <a
      href={client.href}
      target="_blank"
      rel="noopener noreferrer"
      aria-label={`${client.name} website`}
      className="flex w-[280px] max-w-full items-center justify-center overflow-hidden transition-all duration-300 hover:brightness-125"
      onMouseEnter={() => setIsHovered(true)}
      onMouseLeave={() => setIsHovered(false)}
      onFocus={() => setIsHovered(true)}
      onBlur={() => setIsHovered(false)}
      style={{
        padding: `${defaults.spacingPadding}px`,
        marginInline: logoGapX < 0 ? `${logoGapX / 2}px` : undefined,
      }}
    >
      <AsciiLogo
        name={client.name}
        artKey={client.artKey}
        seed={client.seed}
        titleSrc={client.titleSrc}
        titleOffsetY={titleOffsetY}
        params={logoParams}
        isHovered={isHovered}
      />
    </a>
  );
}

